    private void refreshCraftTable() {
    DefaultTableModel model = (DefaultTableModel) Dashboard_table.getModel(); // Use jTable1 (your table name)
    model.setRowCount(0);
    for (Craft c : CraftQueue.getAllCrafts()) {
        model.addRow(new Object[]{
            c.getName(),
            c.getCraftType(),
            c.getEngineType(),
            c.getType(),
            c.getPurpose(),
            c.getStatus()
        });
    }
}
